    private void jMenuItem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem2ActionPerformed
        // TODO add your handling code here:
        jfc = new JFileChooser();
        jfc.showOpenDialog(null);
        File f = jfc.getSelectedFile();
        if(f != null){
            setTitle(f.getName());
            currentlyopened=f;
            try {
            FileReader freader = new FileReader(f);
            BufferedReader reader = new BufferedReader(freader);
            String line;
            String text="";
            while((line=reader.readLine())!=null)
            {
                text=text+line+"\n";
            }
            jTextArea1.setText(text);
            } catch (Exception e) {
            System.err.println("Error : " + e);
        }
      }
        
    }//GEN-LAST:event_jMenuItem2ActionPerformed

    void goTo()
    {
        int lineNumber=0;
        try
        {
            lineNumber=jTextArea1.getLineOfOffset(jTextArea1.getCaretPosition())+1;
            String tempStr=JOptionPane.showInputDialog(f,"Enter Line Number:",""+lineNumber);
            if(tempStr==null)
            {return;}
            lineNumber=Integer.parseInt(tempStr);
            jTextArea1.setCaretPosition(jTextArea1.getLineStartOffset(lineNumber-1));
        }catch(Exception e){}
    }
    private void jMenuItem3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem3ActionPerformed
        // TODO add your handling code here:
        if(currentlyopened!=null){
            try{
            FileWriter fwriter = new FileWriter(currentlyopened);
            BufferedWriter writer = new BufferedWriter(fwriter);
            String text = jTextArea1.getText();
            writer.write(text);
            writer.close(); /*importantnwithout it it wont save*/
            JOptionPane.showMessageDialog(rootPane,"Saved");
            setTitle(currentlyopened.getName());
            }
            catch(Exception ex)
            {
                System.out.println("Error : "+ex);
            }
        }
        else{
            String filename = JOptionPane.showInputDialog("Name this file");
            if(filename != null && !filename.equals("")){
            jfc = new JFileChooser();
            jfc.setDialogTitle("Save As");
            jfc.setSelectedFile(new File(filename + ".txt"));
            int sf = jfc.showSaveDialog(null);
            if (sf == JFileChooser.APPROVE_OPTION) {
                FileWriter out;
                try {
                    File k = jfc.getCurrentDirectory();
                    File fl = new File(k + "\\" + filename + ".txt");
                    currentlyopened = fl;
                    out = new FileWriter(fl);
                    String h = jTextArea1.getText();
                    out.write(h);
                    out.close();
                    setTitle(currentlyopened.getName());
                } 
                catch (IOException ex) {
                    System.out.println("Error : "+ex);
                }
            }
            }
        }
    }//GEN-LAST:event_jMenuItem3ActionPerformed

