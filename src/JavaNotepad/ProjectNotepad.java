    private void formWindowClosing(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowClosing
        // TODO add your handling code here:
        String s = jTextArea1.getText();
        if(currentlyopened == null)
        {
            if(jTextArea1.getText().equals(""))
            {
                System.exit(0);
            }
            else
            {
                Object[] options = {"Save","Don't Save","Cancel"};
                int choice = JOptionPane.showOptionDialog(this,"Do you want to save changes to Untitled ?","JAVA Notepad",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE,null,options,options[0]);
                if(choice == 0)
                {
                     String filename = JOptionPane.showInputDialog("Name this file");
                     if(filename != null && !filename.equals("")){
                     jfc = new JFileChooser();
                     jfc.setDialogTitle("Save As");
                     jfc.setSelectedFile(new File(filename + ".txt")); 
                     int sf = jfc.showSaveDialog(null);
                     if (sf == JFileChooser.APPROVE_OPTION) {
                     FileWriter out;
                     try {
                        File k = jfc.getCurrentDirectory();
                        File fl = new File(k + "\\" + filename + ".txt");
                        currentlyopened = fl;
                        out = new FileWriter(fl);
                        //out = new FileWriter(k + "\\" + filename + ".txt");
                        String h = jTextArea1.getText();
                        out.write(h);
                        out.close();
                        System.exit(0);
                        } catch (IOException ex) {
                            System.out.println("Error : "+ex);
                          }       
                        }
                    }
                }
                else if(choice == 1)
                {
                    System.exit(0);
                }
                else
                {   
                }
            }
        }
        else
        {
            Object[] options = {"Save","Don't Save","Cancel"};
                int choice = JOptionPane.showOptionDialog(this,"Do you want to save changes to Untitled ?","JAVA Notepad",JOptionPane.YES_NO_CANCEL_OPTION,JOptionPane.QUESTION_MESSAGE,null,options,options[0]);
                if(choice == 0)
                {
                     if(currentlyopened!=null){
                     try{
                        FileWriter fwriter = new FileWriter(currentlyopened);
                        BufferedWriter writer = new BufferedWriter(fwriter);
                        String text = jTextArea1.getText();
                        writer.write(text);
                        writer.close(); /*importantnwithout it it wont save*/
                        JOptionPane.showMessageDialog(rootPane,"Saved");
                        setTitle(currentlyopened.getName());
                        System.exit(0);
                    }
                    catch(Exception ex)
                    {
                        System.out.println("Error : "+ex);
                    }
                  } 
                }
                else if(choice == 1)
                {
                    System.exit(0);
                }
                else
                {   
                }
        }
    }//GEN-LAST:event_formWindowClosing

